# Data Type
number = 67  # int
num = 78.94  # float
greeting = "Hello there"  # str
IsPythonInteresting = True  # bool
# A variable  storing  multiple  values
languages = ["Python", "PHP", "Java"]  # list
fruits = ("banana", "apple", "pineapple")
countries = {"Kenya", "China", "North America"}  # set
# Dictionary
details = {
    "firstname": "Brian",
    "age": 18,
    "course": "MIT",
    "nationality": "USA"
}
print(number)
print(IsPythonInteresting)
print(countries)
print(details)
print(details["course"])
# Determining the datatype
print(type(details))
print(type(countries))
print(type(languages))

# Typecasting - Coverting one datatype to another
print(float(number))
print(int(num))
